function catalogName(row: CatalogRow): LocalizedText {
  return text(row.name, row.name, row.name);
}

function catalogAddress(row: CatalogRow): LocalizedText {
  const value = row.address || row.neighborhood || "Netanya";
  return text(value, value, value);
}

function catalogNeighborhood(row: CatalogRow): LocalizedText {
  const value = row.neighborhood || "Netanya";
  return text(value, value, value);
}

function sportList(row: CatalogRow, locale: keyof LocalizedText) {
  return row.sports.map((code) => sportLabels[code][locale]).join(" · ");
}

function catalogSummary(row: CatalogRow): LocalizedText {
  return {
    he: `רשומת קטלוג ספורט בנתניה. ענפים: ${sportList(row, "he")}. יש לאמת לוח זמנים ותנאי הרשמה מול המפעיל.`,
    ru: row.summary || `Каталог SportIL по Нетании. Виды спорта: ${sportList(row, "ru")}. Расписание и условия записи нужно подтвердить у оператора.`,
    en: `Netanya sport catalog entry. Sports: ${sportList(row, "en")}. Schedule and registration conditions must be confirmed with the operator.`,
  };
}

function catalogSourceNotes(row: CatalogRow): LocalizedText {
  const ru = [
    row.actuality,
    row.notes,
    row.phone ? `Телефон: ${row.phone}` : "Телефон не опубликован",
    `Исходная строка Excel: ${row.row}`,
  ].filter(Boolean).join(". ");

  return {
    he: `יובא מקטלוג SportIL. מקור: ${row.sourceUrl}. יש לאמת לפני פתיחת הזמנה אונליין.`,
    ru,
    en: `Imported from the SportIL Netanya catalog. Source: ${row.sourceUrl}. Verify before enabling online booking.`,
  };
}

function catalogImage(row: CatalogRow): FacilityImage {
  return {
    alt: text(
      `תמונה מייצגת: ${sportList(row, "he")}`,
      `Иллюстрация направления: ${sportList(row, "ru")}`,
      `Representative image: ${sportList(row, "en")}`,
    ),
    sourceUrl: row.sourceUrl,
    src: imageByKey[row.imageKey],
  };
}

function catalogSearchTerms(row: CatalogRow) {
  return [
    row.name,
    row.address,
    row.neighborhood,
    row.rawSports,
    row.type,
    row.sector,
    row.phone,
    row.priority,
    ...row.sports.flatMap((code) => [sportLabels[code].ru, sportLabels[code].en, sportLabels[code].he]),
  ].filter(Boolean).map((value) => value.toLocaleLowerCase());
}

